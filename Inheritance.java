
public class Inheritance 
{
	public static void main(String[] args) {
	    
	    Computer obj1=new Computer();
	    obj1.Name("Lenovo");
	    obj1.Ram("8Gb");
	    obj1.Storage("2Tb");
	    obj1.Price(50000.00);
	    
	    System.out.println();
	    
	    Computer obj2=new Computer();
	    obj2.Name("Deal");
	    obj2.Ram("4Gb");
	    obj2.Storage("1Tb");
	    obj2.Price(35000.00);
		
	}
}

class Computer extends Processor{
    
    public void Name(String name){
        
        System.out.println("Name: "+name);
    }
}

class Processor{
    
    public void Ram(String ram){
        System.out.println("Ram: "+ram);
        
    }
    public void Storage(String storage){
        System.out.println("Storage: "+storage);
    }
    public void Price(double x){
        System.out.println("Price: "+x);
    }
}